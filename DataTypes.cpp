#include<iostream>  // Including the standard input-output library

using namespace std; // Using the standard namespace

int main() {
    // Declaration and initialization of different data types in C++

    // Integer (int) - Used for whole numbers
    int age = 25;
    cout << "Age: " << age << endl;

    // Float (float) - Used for numbers with decimal points
    float height = 5.9;
    cout << "Height: " << height << endl;

    // Double (double) - Used for larger decimal numbers with more precision
    double weight = 72.456;
    cout << "Weight: " << weight << endl;

    // Character (char) - Used for storing single characters
    char grade = 'A';
    cout << "Grade: " << grade << endl;

    // String (string) - Used for storing sequences of characters (needs #include <string>)
    string name = "Siva";
    cout << "Name: " << name << endl;

    // Boolean (bool) - Used for true/false values
    bool isCoder = true;
    cout << "Is a coder: " << isCoder << endl;

    // Demonstrating basic arithmetic operations
    int num1 = 10;
    int num2 = 5;

    // Addition
    int sum = num1 + num2;
    cout << "Sum: " << sum << endl;

    // Subtraction
    int difference = num1 - num2;
    cout << "Difference: " << difference << endl;

    // Multiplication
    int product = num1 * num2;
    cout << "Product: " << product << endl;

    // Division
    int quotient = num1 / num2;
    cout << "Quotient: " << quotient << endl;

    // Modulus (remainder of division)
    int remainder = num1 % num2;
    cout << "Remainder: " << remainder << endl;

    return 0;  // End of the program
}
